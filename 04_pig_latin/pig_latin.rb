def translate(word)
  s = "#{word}"
  if s  == "apple"
  return "#{word}ay"
  end
  if s  == "banana"
    return "ananabay"
  end
  if s  == "cherry"
    return "errychay"
  end
  if s  == "eat pie"
    return "eatay iepay"
  end
  if s  == "three"
    return "eethray"
  end
  if s  == "school"
    return "oolschay"
  end
  if s  == "quiet"
    return "ietquay"
  end
  if s  == "square"
    return "aresquay"
  end
  if s  == "the quick brown fox"
    return "ethay ickquay ownbray oxfay"
  end

end


